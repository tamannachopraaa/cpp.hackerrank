# Basic Data Types
#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    // Complete the code.
    int a;
    long int b;
    char c;
    float d;
    double e;
    scanf("%d %ld %c %f %lf",&a,&b,&c,&d,&e);
    printf("%d",a);
    printf("\n%ld",b);
    printf("\n%c",c);
    printf("\n%.2f",d);
    printf("\n%lf",e);
    return 0;
}
